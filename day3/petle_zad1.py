# pętla  - możliwość wykonania kodu wielokrotnie
# for - pętla iteracyjna

for i in range(5):  # od 0 do 4
    print(i)

for i in range(20):
    pass

for _ in range(15):  # niema zmienna
    print("Test podłoga")
    print(_)  # można odczytać wartość

for i in range(5):
    print(i + 2)
    print(i * 2)
